// PostsAPI предоставляет методы для работы с постами в ITD.
type PostsAPI interface {
	// NewFeed создаёт итератор для получения ленты постов.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - limit: количество постов на страницу (рекомендуется 10-50)
	//   - tab: тип сортировки ("popular", "clan", "following")
	// Возвращает FeedIterator для постраничной загрузки постов.
	NewFeed(ctx context.Context, tab FeedTab, limit int) FeedIterator

	// NewUserPosts создаёт итератор для получения постов пользователя.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - username: имя пользователя (без @)
	//   - limit: количество постов на страницу (рекомендуется 10-50)
	// Возвращает FeedIterator для постраничной загрузки постов пользователя.
	NewUserPosts(ctx context.Context, username string, limit int) FeedIterator

	// Get получает пост по его ID.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста
	// Возвращает полную информацию о посте или ошибку при проблемах с сетью/API.
	Get(ctx context.Context, postID string) (*Post, error)

	// Create создаёт новый пост.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - content: текстовое содержимое поста
	//   - attachmentIDs: массив ID вложений (изображения, файлы)
	// Возвращает созданный пост или ошибку при проблемах с сетью/API.
	Create(ctx context.Context, content string, attachmentIDs ...string) (*Post, error)

	// Delete удаляет пост по его ID.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста для удаления
	// Возвращает ошибку при проблемах с сетью/API или если пост не найден.
	Delete(ctx context.Context, postID string) error

	// Like ставит лайк на пост.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста
	// Возвращает обновлённое количество лайков или ошибку при проблемах с сетью/API.
	Like(ctx context.Context, postID string) (*LikesCountResponse, error)

	// Unlike убирает лайк с поста.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста
	// Возвращает обновлённое количество лайков или ошибку при проблемах с сетью/API.
	Unlike(ctx context.Context, postID string) (*LikesCountResponse, error)

	// Repost создаёт репост существующего поста.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста для репоста
	//   - content: опциональный комментарий к репосту (может быть nil)
	// Возвращает созданный репост или ошибку при проблемах с сетью/API.
	Repost(ctx context.Context, postID string, content string) (*Post, error)

	// Vote голосует в опросе, прикреплённом к посту.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста с опросом
	//   - optionIDs: массив ID вариантов ответа для голосования
	// Возвращает обновлённый опрос с результатами или ошибку при проблемах с сетью/API.
	Vote(ctx context.Context, postID string, optionIDs ...string) (*Poll, error)

	// View отмечает пост как просмотренный.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: уникальный идентификатор поста
	// Возвращает результат операции или ошибку при проблемах с сетью/API.
	View(ctx context.Context, postID string) (*PostViewResponse, error)
}

// UserAPI предоставляет методы для работы с пользователями в ITD.
type UserAPI interface {
	// Me получает информацию о текущем аутентифицированном пользователе.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	// Возвращает информацию о текущем пользователе или ошибку при проблемах с аутентификацией.
	Me(ctx context.Context) (*Me, error)

	// Get получает информацию о пользователе по его username.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - username: имя пользователя (без @)
	// Возвращает полную информацию о пользователе или ошибку при проблемах с сетью/API.
	Get(ctx context.Context, username string) (*User, error)

	// Follow подписывается на пользователя.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - username: имя пользователя для подписки
	// Возвращает ошибку при проблемах с сетью/API или если пользователь не найден.
	Follow(ctx context.Context, username string) error

	// Unfollow отписывается от пользователя.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - username: имя пользователя для отписки
	// Возвращает ошибку при проблемах с сетью/API или если пользователь не найден.
	Unfollow(ctx context.Context, username string) error

	// GetFollowers возвращает список подписчиков пользователя.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - username: идентификатор пользователя или юзернейм пользователя
	//   - limit: максимальное количество подписчиков в ответе
	//
	// Возвращает срез UserCompact с данными подписчиков.
	//
	// Примечание: в настоящее время API возвращает не более 20 подписчиков
	// независимо от переданного limit.
	// Возвращает срез UserCompact с данными подписчиков. UserCompact с данными подписок.
	GetFollowers(ctx context.Context, username string, limit int) ([]UserCompact, error)

	// GetFollowing возвращает список подписок пользователя.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - username: идентификатор пользователя или юзернейм пользователя
	//   - limit: максимальное количество подписок в ответе
	//
	// Возвращает срез
	//
	// Примечание: в настоящее время API возвращает не более 20 подписчиков
	// независимо от переданного limit.
	// Возвращает срез UserCompact с данными подписчиков. UserCompact с данными подписок.
	GetFollowing(ctx context.Context, username string, limit int) ([]UserCompact, error)

	// UpdateProfile обновляет профиль текущего пользователя.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - displayName: новое отображаемое имя (nil = не изменять)
	//   - username: новое имя пользователя (nil = не изменять)
	//   - bio: новая биография (nil = не изменять)
	//   - bannerID: ID нового баннера (nil = не изменять)
	// Возвращает обновлённую информацию о пользователе или ошибку при проблемах с сетью/API.
	UpdateProfile(ctx context.Context, displayName *string, username *string, bio *string, bannerID *string) (*UpdateProfileResponse, error)
}

// CommentsAPI предоставляет методы для работы с комментариями в ITD.
type CommentsAPI interface {

	// NewCommentList создаёт итератор для получения комментариев к посту.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: идентификатор поста
	//   - limit: количество комментариев на страницу (рекомендуется 10-20)
	// Возвращает CommentIterator для постраничной загрузки комментариев.
	NewCommentList(ctx context.Context, postID string, limit int) CommentIterator

	// ListReplies получает список ответов на комментарий.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - commentID: идентификатор комментария
	//   - limit: количество ответов на страницу
	// Возвращает массив ответов или ошибку при проблемах с сетью/API.
	ListReplies(ctx context.Context, commentID string, limit int) ([]*Comment, error)

	// CreateComment создаёт новый комментарий к посту.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - postID: идентификатор поста
	//   - content: текстовое содержимое комментария
	//   - attachmentIDs: массив ID вложений (может быть nil)
	// Возвращает созданный комментарий или ошибку при проблемах с сетью/API.
	CreateComment(ctx context.Context, postID string, content string, attachmentIDs []string) (*CreateComment, error)

	// CreateReply создаёт ответ на комментарий.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - commentID: идентификатор комментария для ответа
	//   - content: текстовое содержимое ответа
	//   - attachmentIDs: массив ID вложений (может быть nil)
	// Возвращает созданный ответ или ошибку при проблемах с сетью/API.
	CreateReply(ctx context.Context, parentCommentID, replyToUserID, content string, attachmentIDs []string) (*CreateComment, error)

	// Delete удаляет комментарий по его ID.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - commentID: идентификатор комментария для удаления
	// Возвращает ошибку при проблемах с сетью/API или если комментарий не найден.
	Delete(ctx context.Context, commentID string) error

	// Like ставит лайк на комментарий.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - commentID: идентификатор комментария
	// Возвращает ошибку при проблемах с сетью/API.
	Like(ctx context.Context, commentID string) error

	// Unlike убирает лайк с комментария.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - commentID: идентификатор комментария
	// Возвращает ошибку при проблемах с сетью/API.
	Unlike(ctx context.Context, commentID string) error

	// Update обновляет содержимое комментария.
	// Параметры:
	//   - ctx: контекст для управления временем жизни запроса
	//   - commentID: идентификатор комментария
	//   - content: новое текстовое содержимое
	// Возвращает обновлённый комментарий или ошибку при проблемах с сетью/API.
	Update(ctx context.Context, commentID string, content string) (*CommentUpdate, error)
}

// Iterator предоставляет generic интерфейс для постраничной загрузки данных.
// Использует паттерн итератора для последовательного получения страниц.
type Iterator[T any] interface {
	// HasMore возвращает true, если есть ещё данные для загрузки.
	// Возвращает false, когда достигнут конец.
	HasMore() bool

	// Next загружает и возвращает следующую страницу данных.
	// Автоматически обновляет внутренний курсор для следующего вызова.
	// Возвращает ошибку при проблемах с сетью или API.
	Next() ([]T, error)
}

// FeedIterator предоставляет интерфейс для постраничной загрузки постов.
// Использует паттерн итератора для последовательного получения страниц ленты.
type FeedIterator = Iterator[*Post]

type CommentIterator = Iterator[*Comment]
